package com.sdmx.framework.vo;

public class TaskNumInfo {
	private int consultNum;
	private int complaitNum;
	private int adviceNum;
	private int incidentNum;
	private int researchNum;
	/**
	 * 待处理的问卷调查任务数
	 */
	private int questionnaireNum;
	
	public int getConsultNum() {
		return consultNum;
	}
	public void setConsultNum(int consultNum) {
		this.consultNum = consultNum;
	}
	public int getComplaitNum() {
		return complaitNum;
	}
	public void setComplaitNum(int complaitNum) {
		this.complaitNum = complaitNum;
	}
	public int getAdviceNum() {
		return adviceNum;
	}
	public void setAdviceNum(int adviceNum) {
		this.adviceNum = adviceNum;
	}
	public int getIncidentNum() {
		return incidentNum;
	}
	public void setIncidentNum(int incidentNum) {
		this.incidentNum = incidentNum;
	}
	public int getResearchNum() {
		return researchNum;
	}
	public void setResearchNum(int researchNum) {
		this.researchNum = researchNum;
	}
	public int getQuestionnaireNum() {
		return questionnaireNum;
	}
	public void setQuestionnaireNum(int questionnaireNum) {
		this.questionnaireNum = questionnaireNum;
	}
	
	
}
